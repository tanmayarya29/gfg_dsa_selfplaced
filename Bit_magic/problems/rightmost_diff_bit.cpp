class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here
        //find xor and find first set bit from right
        int XOR=m^n;
        return floor(log2(XOR&(-XOR))+1);
    }
};

